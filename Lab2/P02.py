# Variables and Data type
# ตัวแปร, กล่องบรรจุ data

# text : string
str1 = "Am"
str2 = "I"

print(str1, str2)
print(str1 + str2)


a = 20
b = 40.5

print("I am %d." % a) # formatted string
# %d : int, %f : float, %s : string
print("I am %d. My weight is %f kg." % (a,b))
print("I am %d. My weight is %.2f kg." % (a,b)) 
# %.2f ทศนิยม 2 ตำแหน่ง

